# 步行到公交站的时间，单位为分钟
a = 15
# 乘坐公交的时间，1小时15分钟转换为分钟
b = 60 + 15
# 计算公交通勤总时间
c = a + b

# 开车的时间，1小时30分钟转换为分钟
d = 60 + 30
# 从停车场步行的时间，单位为分钟
e = 5
# 计算开车通勤总时间
f = d + e

# 比较两种通勤方式的总时间
if c < f:
    print("公交通勤更快，公交通勤总时间为{}分钟，开车通勤总时间为{}分钟".format(c, f))
elif c > f:
    print("开车通勤更快，公交通勤总时间为{}分钟，开车通勤总时间为{}分钟".format(c, f))
else:
    print("两种通勤方式时间相同，总时间均为{}分钟".format(c))